package GameModifiers;

/*************************************
 This enum stores all of the different
 IDs for the game objects in the
 program. It allows game logic to
 easily identify the object type
 without extracting the class type.
 ************************************/
public enum ID {
    Player1(),
    Player2(),
    Nature(),
    RadiantTower(),
    RadiantFarm(),
    RadiantBase(),
    RadiantFireBall(),
    RadiantCreep(),
    CreepCorpse(),
    DeadTower(),
    DireTower(),
    DireFarm(),
    DireFireBall(),
    DireBase(),
    DireCreep(),
    Explosion(),
    Wall(),
    Powerup(),
    FireAnimation()
}
